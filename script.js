const answers = [
    "Sai Baba says: Have faith and patience.",
    "Sai Baba says: Help others selflessly.",
    "Sai Baba says: Trust in me and your prayers will be answered.",
    "Sai Baba says: Maintain peace and love in your heart.",
    // Add more answers as needed
];

function showAnswer() {
    const randomIndex = Math.floor(Math.random() * answers.length);
    const answer = answers[randomIndex];
    document.getElementById('devotional-answer').textContent = answer;
    document.getElementById('answer').style.display = 'block';
}

function closeModal() {
    document.getElementById('answer').style.display = 'none';
}
